# Farhan, Maaz, Saad, Daniyal, aur Faraz  dost hain.
def main():
   farhan = 18
   maaz = -1 + farhan
   saad = 0 + maaz
   daniyal = farhan
   faraz = saad

   print(f"Farhan is {farhan}")
